public class person {

  private String name;
  private String email;
  private String description;
 
 public person (String name, String email){
     this.name= name;
     this.email= email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getDescription() {
        return description;
    }
    
}